package com.amarukael.bloodbags.Model;

public class StokModel {
    private String namars, jalanrs;
    private int gambarrs, jarakrs, stok;

    public String getNamars() {
        return namars;
    }

    public void setNamars(String namars) {
        this.namars = namars;
    }

    public String getJalanrs() {
        return jalanrs;
    }

    public void setJalanrs(String jalanrs) {
        this.jalanrs = jalanrs;
    }

    public int getGambarrs() {
        return gambarrs;
    }

    public void setGambarrs(int gambarrs) {
        this.gambarrs = gambarrs;
    }

    public int getJarakrs() {
        return jarakrs;
    }

    public void setJarakrs(int jarakrs) {
        this.jarakrs = jarakrs;
    }

    public int getStok() {
        return stok;
    }

    public void setStok(int stok) {
        this.stok = stok;
    }
}
